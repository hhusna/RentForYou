    <h2 class="section-title">Syarat & Ketentuan</h2>
    <p class="section-subtitle">Mohon dibaca dengan saksama sebelum melakukan penyewaan.</p>
    
    <div class="sk-list">
        <ul>
            <li><strong>Jaminan Sewa:</strong> Penyewa wajib menitipkan e-KTP asli yang masih berlaku sebagai jaminan selama masa penyewaan.</li>
            <li><strong>Surat Izin Mengemudi:</strong> Penyewa wajib memiliki dan dapat menunjukkan SIM C asli yang masih berlaku.</li>
            <li><strong>Batas Waktu Sewa:</strong> Durasi sewa dihitung per 24 jam. Keterlambatan pengembalian akan dikenakan denda sesuai tarif yang berlaku.</li>
            <li><strong>Kerusakan & Kehilangan:</strong> Segala bentuk kerusakan atau kehilangan unit motor beserta kelengkapannya (helm, jas hujan) menjadi tanggung jawab penuh penyewa.</li>
            <li><strong>Penggunaan:</strong> Motor sewaan tidak diizinkan untuk dipindahtangankan, digadaikan, atau digunakan untuk kegiatan melanggar hukum.</li>
        </ul>
    </div>
</section>
